CREATE TABLE `isms_software` (
  `id` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `name_zh` varchar(500) NOT NULL COMMENT '软件中文名称',
  `name_en` varchar(500) NOT NULL COMMENT '软件英文名称',
  `version` varchar(100) NOT NULL COMMENT '版本号（如v6.2.10.23 SP10）',
  `release_date` DATE DEFAULT NULL COMMENT '发布日期',
  `developer_id` INT UNSIGNED NOT NULL COMMENT '开发商ID（关联isms_developer.id，代码逻辑维护）',
  `country_id` SMALLINT UNSIGNED NOT NULL COMMENT '产地国家ID（关联isms_country.id，代码逻辑维护）',
  `sys_req` text COMMENT '系统要求（处理器/内存等）',
  `description` text COMMENT '软件描述',
  `size_gb` DECIMAL(10,2) DEFAULT NULL COMMENT '软件大小（GB）',
  `deployment_method` varchar(100) DEFAULT NULL COMMENT '部署方式（如单机/云端）',
  `compliance_info` text COMMENT '合规性信息',
  `security_info` text COMMENT '安全信息',
  `intellectual_property` text COMMENT '知识产权信息',
  `status` TINYINT NOT NULL DEFAULT 1 COMMENT '状态（1：有效，0：下架）',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_name_version` (`name_zh`, `version`) COMMENT '同一软件版本唯一',
  KEY `idx_developer` (`developer_id`),
  KEY `idx_country` (`country_id`),
  KEY `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='工业软件主表';