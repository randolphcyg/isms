CREATE TABLE `isms_software` (
  `id` INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '自增ID',
  `name_zh` varchar(200) NOT NULL COMMENT '软件中文名称（不含特殊符号，如：中望CAD）',
  `name_en` varchar(200) NOT NULL COMMENT '软件英文名称（不含特殊符号，如：ZWCAD）',
  `version` varchar(100) NOT NULL COMMENT '版本号（统一小写，前缀v可选，如v6.2.10、6.2）',
  `release_year` YEAR DEFAULT NULL COMMENT '发布年份（如2023）',
  `release_month` TINYINT UNSIGNED DEFAULT NULL COMMENT '发布月份（1-12，NULL表示仅精确到年）',
  `release_day` TINYINT UNSIGNED DEFAULT NULL COMMENT '发布日（1-31，NULL表示仅精确到年或月）',
  `developer_id` INT UNSIGNED NOT NULL COMMENT '开发商ID（关联isms_developer.id）',
  `cpu_req` varchar(200) DEFAULT NULL COMMENT '处理器要求（如：Intel i5及以上）',
  `memory_min_gb` DECIMAL(6,2) DEFAULT NULL COMMENT '最小内存要求（GB）',
  `disk_min_gb` DECIMAL(6,2) DEFAULT NULL COMMENT '最小磁盘空间（GB）',
  `sys_req_other` text COMMENT '其他系统要求（如显卡、网络）',
  `size_bytes` BIGINT UNSIGNED DEFAULT NULL COMMENT '软件实际大小（字节，用于计算和存储，1KB=1024字节）',
  `description` text COMMENT '软件描述（功能、应用场景等）',
  `deployment_method` ENUM('standalone','cloud','hybrid') DEFAULT NULL COMMENT '部署方式（standalone：单机；cloud：云端；hybrid：混合）',
  `compliance_info` text COMMENT '合规性信息（如认证标准、行业规范）',
  `security_info` text COMMENT '安全信息（如加密方式、漏洞修复记录）',
  `intellectual_property` text COMMENT '知识产权信息（如专利号、著作权登记号）',
  `status` ENUM('active','inactive','testing','discontinued') NOT NULL DEFAULT 'active' COMMENT '状态（active：有效；inactive：下架；testing：测试中；discontinued：停止维护）',
  `bit_widths` SET('8bit', '16bit', '32bit', '64bit', '128bit', '256bit', '512bit') DEFAULT NULL COMMENT '支持的位宽集合',
  `source_url` VARCHAR(500) DEFAULT NULL COMMENT '来源页面URL',
  `download_link` VARCHAR(1000) DEFAULT NULL COMMENT '下载链接（种子地址等）',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_name_version` (`name_en`, `version`) COMMENT '同一软件的版本唯一',
  KEY `idx_developer` (`developer_id`),
  KEY `idx_country` (`country_id`),
  KEY `idx_status` (`status`),
  KEY `idx_release_date` (`release_year`, `release_month`, `release_day`) COMMENT '按发布时间筛选'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='工业软件主表';